package vn.techlifegroup.wesell.model;

public class Functions {

    public Functions() {
    }

    int icon;
    String name;

    public Functions(int icon, String name) {
        this.icon = icon;
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }
}

